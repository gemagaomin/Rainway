    /**
     * Saves a JPEG {@link Image} into the specified {@link File}.
     */
    private static class ImageSaver implements Runnable {

        /**
         * The JPEG image
         */
        private final Image mImage;
        /**
         * The file we save the image into.
         */
        private final File mFile;
        private Size previewSize;
        private int mWidth;
        private int mHeight;
        private int mSensorOrientation;
        private int rotation;

        ImageSaver(Image image, File file,Size mPreviewSize,int width,int height,int sensorOrientation,int mRotation ) {
            mImage = image;
            mFile = file;
            previewSize=mPreviewSize;
            mWidth=width;
            mHeight=height;
            mSensorOrientation=sensorOrientation;
            rotation=mRotation;
        }

        @Override
        public void run() {
            ByteBuffer buffer = mImage.getPlanes()[0].getBuffer();
            byte[] bytes = new byte[buffer.remaining()];
            buffer.get(bytes);
            FileOutputStream output = null;
            try {
                if(orientationCamera){
                    Bitmap temp= BitmapFactory.decodeByteArray(bytes,0,bytes.length);
                    Matrix matrix = new Matrix();
                    matrix.postScale(-1,1);
                    Bitmap save=Bitmap.createBitmap(temp,0,0,temp.getWidth(),temp.getHeight(),matrix,true);
                    output=new FileOutputStream(mFile);
                    save.compress(Bitmap.CompressFormat.JPEG,100,output);
                }else{
                    output = new FileOutputStream(mFile);
                    output.write(bytes);
                }

            } catch (IOException e) {
                e.printStackTrace();
            } finally {
                mImage.close();
                if (null != output) {
                    try {
                        output.close();
                    } catch (IOException e) {
                        e.printStackTrace();
                    }
                }
            }
        }

    }

